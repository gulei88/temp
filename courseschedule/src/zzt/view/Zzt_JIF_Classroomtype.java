	public void fillroomtype() {
		//首先给ct_id变量设为初始0
		int ct_id = 0;
		//声明DefaultTableModel 得到jtble1表格上的模型
		DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
		//设置表格的行数从0开始计数
		dtm.setRowCount(0);
		//初始化ArrayList列表，通过ct_id主键查询classroomtype（部门表）
		ArrayList<Classroomtype> alist = Classrommtypeaccess
				.getClassroomtypes(ct_id);
		// 遍历教师类型的数组列表
		for (int i = 0; i < alist.size(); i++) {
			// 取出对应字段信息
			int ct_id_zzt = alist.get(i).getCt_id();
			String ct_name = alist.get(i).getCt_name();
			// 生成向量类型变量
			Vector v = new Vector();
			v.add(ct_id_zzt);
			v.add(ct_name);
			// 将向量添加到表格中
			dtm.addRow(v);
		}
	}

