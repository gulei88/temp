	private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {
		// TODO add your handling code here:
		String ct_name = jTextField2.getText();
		// 如果教师类型名称文本框内容为空
		if (ct_name.equals("")) {
			// 提示信息：教师类型名称不能为空！
			JOptionPane.showMessageDialog(this, "部门名称不能为空！");
			jTextField2.requestFocus();
			return;
		}

		Connection con = null;
		int zhujian = 0;
		int ct_int = 0;
		try {
			// 创建数据库连接
			con = Databaseconnection.getconnection();
			//把查询教师类型编号的最大值存入max的结果集
			ResultSet max = Classrommtypeaccess.findmax(con);
			//遍历结果集的所有数据找到最大值赋值给zhujian变量
			while (max.next()) {
				zhujian = max.getInt("ct_id");
			}
			//如果部门编号没用数据,则添加一条编号为“01”的数据
			if (zhujian == 0) {
				ct_int = 1;
			} else {
				//定义一个整形变量,从来存放当前部门编号里面的最大值加一
				ct_int = (zhujian) + 1;
				//在把已经加过一的整形变量转换为字符串放在d_str变量里
			}
			//实例化教师类型的构造方法
			Classroomtype cy = new Classroomtype(ct_int, ct_name);
			// 设置数据库的自动提交方式为false
			con.setAutoCommit(false);
			// 访问数据库查询教师类型名称字段值,如果当前数据库对应的教师类型名称字段里面的值等于用户当前已经输入的值的情况下
			if (Classrommtypeaccess.findname(con, ct_name)) {
				// 提示信息：类型名称已经存在
				JOptionPane.showMessageDialog(this, "部门名称已经存在请重新输入", "错误信息",
						JOptionPane.ERROR_MESSAGE);
				// 教师类型名称文本框获得焦点
				jTextField2.requestFocus();
				// 教师类型名称文本框全选
				jTextField2.selectAll();
				return;
			}
			// 定义r变量存放结果集
			int r = Classrommtypeaccess.insert(con, cy);
			// 如果r>=1
			if (r >= 1) {
				// 则提示信息：添加成功！
				JOptionPane.showMessageDialog(this, "添加成功！");
				// 开始数据库的手动提交方式
				con.commit();
				fillroomtype();
				jTextField1.setText("");
				jTextField2.setText("");
			}
			// 如果r大于1则提示信息部门编号插入失败，请联系系统管理员！
			if (r < 1) {

				Tools.connectionroolback(con, "部门编号插入失败，请联系系统管理员！");
				return;
			}
		} catch (ClassNotFoundException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		} catch (SQLException e) {
		} finally {
			try {
				con.rollback();
				if (!con.isClosed())
					con.close();
			} catch (SQLException e) {
				// TODO Auto-generated catch block
				e.printStackTrace();
			}
		}

	}

